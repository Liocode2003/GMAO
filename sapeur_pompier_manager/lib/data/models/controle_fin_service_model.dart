    return {
      'id': id,
      'sapeur_pompier_id': sapeurPompierId,
      'date_radiation': dateRadiation?.toIso8601String(),
      'lieu_examen': lieuExamen,
      'etat_sante': etatSante,
      'atteint_de': atteintDe,
      'hospitalise_a': hospitaliseA,
      'poids': poids,
